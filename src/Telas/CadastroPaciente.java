    private void Salvar_CadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Salvar_CadastroActionPerformed
        try {
        Paciente p = new Paciente();
        Datas d = new Datas();
        d.setDia(Integer.valueOf(jdia.getSelectedItem().toString()));
        d.setMes(Integer.valueOf(jmes.getSelectedItem().toString()));
        d.setAno(Integer.valueOf(jano.getSelectedItem().toString()));
        
        p.setNome(jnome.getText());
        p.setNumDocIdent(jBi.getText());
        p.setContacto(jcontacto.getText());
        p.setGenero(jsexo.getSelectedItem().toString());
        System.out.println(jsexo.getSelectedItem().toString());
         System.out.println(p.getGenero());
        p.setContactoAlt(jcontactop.getText());
        p.setEndereco(jmorada.getText());
        p.setDataNascimento(d);
        pacienteOpc pc = new pacienteOpc();
           
        pc.cadastrar(p);
             
        
        } catch (Exception ex) {
            Logger.getLogger(CadastroPaciente.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_Salvar_CadastroActionPerformed

